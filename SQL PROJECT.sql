DROP DATABASE IF EXISTS animewatchlist;
CREATE DATABASE animewatchlist;
USE animewatchlist;
CREATE TABLE Users (
    UserID INT PRIMARY KEY AUTO_INCREMENT,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Email VARCHAR(100) UNIQUE,
    JoinDate DATE
);

CREATE TABLE Anime (
    AnimeID INT PRIMARY KEY AUTO_INCREMENT,
    Title VARCHAR(100),
    GenreID INT,
    Episodes INT,
    Season VARCHAR(20),
    Studio VARCHAR(50),
    ReleaseYear INT
);

CREATE TABLE Genres (
    GenreID INT PRIMARY KEY AUTO_INCREMENT,
    GenreName VARCHAR(50) UNIQUE
);

CREATE TABLE Watchlist (
    WatchlistID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    AnimeID INT,
    Status ENUM('Watching','Completed','Plan to Watch'),
    StartDate DATE,
    EndDate DATE,
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (AnimeID) REFERENCES Anime(AnimeID)
);
CREATE TABLE Ratings (
    RatingID INT PRIMARY KEY AUTO_INCREMENT,
    UserID INT,
    AnimeID INT,
    Rating INT CHECK (Rating BETWEEN 1 AND 10),
    Review TEXT,
    RatingDate DATE,
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (AnimeID) REFERENCES Anime(AnimeID)
);
